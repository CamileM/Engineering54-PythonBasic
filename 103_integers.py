# Numerical Types - Integers and Floats

# Integers are whole numbers
# Full numbers,
print(10)
print(type(10))

print(" ")

# Floats are decimal numbers
print(10.0)
print(type(10.0))

# add
print(3 + 4)

# subtract
print(4 - 3)

# multiplication
print(3 * 4)

# division
print(12 / 4)

# Modules - gives you a remainder 
print(10 % 3)