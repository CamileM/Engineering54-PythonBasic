
# If Functions

# Syntax
# if <condition>:
    # block of code
# elif <condition>
    # block of code

weather = 'Sunny'

if weather == 'Rainy':
    print('Take your umbrella')
elif weather == 'Sunny':
    print('Good day today, take some shades out')
else:
    print('Did not catch that. Could you repeat')

